// Helper functions
export const getSchoolById = (schoolId: string) => {
  return LPU_DATA.schools.find(school => school.id === schoolId);
};

export const getProgramsBySchool = (schoolId: string) => {
  const school = getSchoolById(schoolId);
  return school?.programs || [];
};

export const getAllPrograms = () => {
  return LPU_DATA.schools.flatMap(school => 
    (school.programs || []).map(program => ({
      schoolId: school.id,
      schoolName: school.name,
      program
    }))
  );
};

export const searchPrograms = (query: string) => {
  const allPrograms = getAllPrograms();
  const lowerQuery = query.toLowerCase();
  return allPrograms.filter(item => 
    item.program.toLowerCase().includes(lowerQuery) ||
    item.schoolName.toLowerCase().includes(lowerQuery)
  );
};
